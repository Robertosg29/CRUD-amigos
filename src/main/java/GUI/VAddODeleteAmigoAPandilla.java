    private void bAddDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAddDeleteActionPerformed
        ResultSet rs=null;
        int codPandilla=0;
        String nombrePandilla=(String)comboPandillas.getSelectedItem();
        if (añadir) {
            try {
                //hacemos esta consulta para saber si este amigo existe en la pandilla
                rs=p.gb.buscarAmigoPorPandilla(nombre,nombrePandilla);
                if(rs.next()){
                    JOptionPane.showMessageDialog(this,"Ya existe este amigo en esa pandilla.");
                }else{
                  codPandilla=p.gb.consultarPandillasPorNombre(nombrePandilla);
                  p.gb.agregarAmigoAPandilla(cod, codPandilla);
                  JOptionPane.showMessageDialog(this,"Has añadido a "+nombre+" a la pandilla "+nombrePandilla);
                  this.dispose();
                }
            } catch (SQLException ex) {
                Logger.getLogger(VAddODeleteAmigoAPandilla.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else{
            codPandilla=p.gb.consultarPandillasPorNombre(nombrePandilla);
            if(JOptionPane.showConfirmDialog(this,"¿Quieres eliminar a "+nombre+" de la pandilla "+nombrePandilla+"?","¿?",JOptionPane.YES_NO_OPTION)==0){
            p.gb.borrarAmigodePandilla(cod, codPandilla);
            this.dispose();
            }
        }

    }//GEN-LAST:event_bAddDeleteActionPerformed

