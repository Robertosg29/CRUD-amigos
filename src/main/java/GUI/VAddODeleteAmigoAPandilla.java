    private void minitComponents() {

        this.setLocationRelativeTo(p.v);
        this.setDefaultCloseOperation(DISPOSE_ON_CLOSE);
        cargarCombo();
        etiquetaNombre.setText(nombre);
        if (añadir) {
            this.setTitle("Añadir amigo a pandilla");
            bAddDelete.setText("Añadir");
        } else {
            this.setTitle("Eliminar amigo de pandilla");
            bAddDelete.setText("Eliminar");
        }
        this.setResizable(false);
    }

    public void cargarCombo() {
        ResultSet rs;
        if (añadir) {
            try {
                rs = p.gb.consultarPandillas();
                while (rs.next()) {
                    comboPandillas.addItem(rs.getString(2));
                }
            } catch (SQLException ex) {
                Logger.getLogger(VAddODeleteAmigoAPandilla.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            try {
                rs = p.gb.pandillasDeUnAmigo(cod);
                while (rs.next()) {
                    comboPandillas.addItem(rs.getString(1));
                }
            } catch (SQLException ex) {
                Logger.getLogger(VAddODeleteAmigoAPandilla.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }
